package com.sun.finalwork.bean;

public class ceshi {
    public String name;
    public Integer score;
    public Integer age;

    public ceshi() {
    }

    public ceshi(String name, Integer score, Integer age) {
        this.name = name;
        this.score = score;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getScore() {
        return score;
    }

    public void setScore(Integer score) {
        this.score = score;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
